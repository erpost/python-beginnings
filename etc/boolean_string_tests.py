print("python".isalpha())

# Alphabetical and Character Check
print("3rd".isalnum())

# Starts With Check
print("Save".startswith("S"))

# Lowercase Check
print("Hey".islower())

# Uppercase Check
print("HEY".isupper())

# Check to see if every first letter of a word is capitalized
print("The End".istitle())

